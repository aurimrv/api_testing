public class run01_RestErrorResponseTest {
    private static final SutHandler controller = new em.embedded.market.EmbeddedEvoMasterController();
    private static String baseUrlOfSut;

    @BeforeAll
    public static void initClass() {
        controller.setupForGeneratedTest();
        baseUrlOfSut = controller.startSut();
        controller.registerOrExecuteInitSqlCommandsIfNeeded();
        assertNotNull(baseUrlOfSut);
        RestAssured.enableLoggingOfRequestAndResponseIfValidationFails();
        RestAssured.useRelaxedHTTPSValidation();
        RestAssured.urlEncodingEnabled = false;
        RestAssured.config = RestAssured.config()
            .jsonConfig(JsonConfig.jsonConfig().numberReturnType(JsonPathConfig.NumberReturnType.DOUBLE))
            .redirect(redirectConfig().followRedirects(false));
    }

    @AfterAll
    public static void tearDown() {
        controller.stopSut();
    }

    @BeforeEach
    public void initTest() {
        controller.resetDatabase(Arrays.asList("USER_ROLE","CUSTOMER_ORDER","CART_ITEM","PRODUCT","CART","CONTACTS"));
        controller.resetStateOfSUT();
    }

    @Test
    public void testRestErrorResponse() {
        // Assuming RestErrorResponse is a class defined within the project under test
        // If not, this needs to be defined or imported appropriately.
        // Correcting the test case by assuming import/definition of RestErrorResponse
        RestErrorResponse errorResponse = new RestErrorResponse("Error occurred", "Customer", "Error description");
        errorResponse.addFieldError("name", "Invalid name");

        assertNotNull(errorResponse.getMessage());
        assertNotNull(errorResponse.getDescription());
        assertNotNull(errorResponse.getEntityName());
        assertFalse(errorResponse.getFieldErrors().isEmpty());

        assertEquals("Error occurred", errorResponse.getMessage());
        assertEquals("Error description", errorResponse.getDescription());
        assertEquals("Customer", errorResponse.getEntityName());
        assertEquals(1, errorResponse.getFieldErrors().size());
        assertEquals("name", errorResponse.getFieldErrors().get(0).getField());
        assertEquals("Invalid name", errorResponse.getFieldErrors().get(0).getMessage());
    }
}
